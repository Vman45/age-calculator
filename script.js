// Prompt to gather the user's age
var userAge = prompt("How old are you?");

// Calculates the total amount of days based on the users' age
var userAgeInDays = userAge * 365;

// Retrieves the calculated number of days based on the user's age 
// and shows it to the user as an alert on the page
alert(userAge + " years is roughly " + userAgeInDays + " " + "days");